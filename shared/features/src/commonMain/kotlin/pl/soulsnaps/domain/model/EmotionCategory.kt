package pl.soulsnaps.domain.model

/**
 * Enum representing the 8 basic emotions in Plutchik's Wheel of Emotions.
 * These are the primary emotions that form the foundation of the emotion wheel.
 */
enum class EmotionCategory {
    JOY,        // Radość
    TRUST,      // Zaufanie
    FEAR,       // Strach
    SURPRISE,   // Zaskoczenie
    SADNESS,    // Smutek
    DISGUST,    // Wstręt
    ANGER,      // Złość
    ANTICIPATION // Antycypacja
} 